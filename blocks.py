    def __init__(self, x, y):
        self._id = random.randint(1, len(self._rotations))
        self._pos = 0
        self.rot = self._rotations[self._id - 1][self._pos]
        self.vertical = x  # keep track of left right corner of piece
        self.horizontal = y

    def rotate(self, side):  # side = 1 rotate, side = -1 reverse rotation
        self._pos = (self._pos + side) % len(self._rotations[self._id - 1])
        self.rot = self._rotations[self._id - 1][self._pos]

    def move_sides(self, side):
        self.horizontal += side

    def move_down(self):
        self.vertical += 1
